public class Typecasting {
    public static void main(String[] args) {
        byte b  = 43;
        char c = 6;
        int i = 20;
        double d = 4.00392393;
        float f =10.222332f;
        short s =3244;
        double result= (b*c)+(i+d)-(f*s);
        System.out.println((b*c)+" "+(i+d)+" "+(f*s));
        System.out.println(result);
    }
}
